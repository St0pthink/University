from math import *


x = float(input("Введите значение x:"))
t = float(input("Введите значение t:"))
f = ( (1 + x**2)**(1/2) - 2*x*t) / ( (1 + (1 + sin(x)**2 + 0.3 * cos(x*t)**2 ) **(1/2) ) **(1/2) )  
print("Значение функции f(x;t) в точке x= {:.2f} и в точке t = {:.2f} равно {:.2f}".format(x,t,f))